// alert(fib(77)); // 5527939700884757
// P.S. Функція повинна бути швидкою. Виклик fib(77) має тривати не більше частки секунди

function fib(n) {
    let a = 1;
    let b = 1;
    for (let i = 3; i <= n; i++) {
        let c = a + b;
        a = b;
        b = c;
    }
    return b;
}
  

